export async function analyzeDrawing(base64Image: string, apiKey: string): Promise<string> {
  try {
    const ai = new GoogleGenAI({ apiKey });
    // Remove data:image/...;base64, prefix if present
    const cleanBase64 = base64Image.replace(/^data:image\/(png|jpeg|jpg);base64,/, '');

    const response = await ai.models.generateContent({
      model: 'gemini-2.5-flash',
      contents: [
        SYSTEM_INSTRUCTION,
        {
          inlineData: {
            data: cleanBase64,
            mimeType: 'image/jpeg'
          }
        }
      ],
      config: {
        temperature: 0.4,
      }
    });

    return response.text || '결과를 생성하지 못했습니다. 다시 시도해주세요.';
  } catch (error: any) {
    console.error('Gemini API Error:', error);
    throw new Error(error.message || '이미지 분석 중 오류가 발생했습니다. API 키나 이미지를 확인해주세요.');
  }
}
